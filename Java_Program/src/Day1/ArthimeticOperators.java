package Day1;
/*

Operators - Used to perform some Operations on variables/values

Arthimetic Operators - used to perform basic math operations

add +
sub -
mul *
div /
mod % remainder
 */
public class ArthimeticOperators {
    public static void main(String[] args) {

        int a = 5;
        int b = 3;

        System.out.println(a+b);//5+3
        System.out.println(a-b); //2
        System.out.println(a*b); //15
        System.out.println(a/b); //1 - quotient
        System.out.println(a%b); //2

        System.out.println(a+5);

    }
}
