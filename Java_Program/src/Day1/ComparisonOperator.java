package Day1;

public class ComparisonOperator {
    public static void main(String[] args) {
        //compares two values and gives the output in boolean (true/false)
        int a = 10;
        int b = 20;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);

    }
}
